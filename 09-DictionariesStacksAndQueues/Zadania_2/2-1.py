# Set Operations:

# 1. & (Intersection): Finds common elements between sets.
# 2. | (Union): Combines all elements from both sets (removes duplicates).
# 3. - (Difference): Finds elements present in one set but not the other.
# 4. ^ (Symmetric Difference): Finds elements present in either set but not in both.
# 5. .issubset(): Checks if one set is a subset of another.
