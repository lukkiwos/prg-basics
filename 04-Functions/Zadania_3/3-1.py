# To use the functions available in an external module,
# you must include that module in your program.
import math

square_root = math.sqrt(7)
print(f"A square root of 7 is: {square_root:.2f}")

natural_logarithm = math.log10(5)
print(f"A natural logarithm of 5 is: {natural_logarithm:.2f}")

x = math.radians(90)
sine = math.sin(x)
print(f"A sine of 90 degrees is: {sine}")